<p>1)Definition:
Blood donation refers to the process of collecting, testing, preparing, and storing blood and blood components. Donors are most commonly unpaid volunteers, but they may also be paid by commercial enterprises. Blood registry refers to the collection and sharing of data about donated blood and ineligible donors.</p>

<br>

<p>2)Key terms:-
Apheresis — Extraction of a specific component from donated blood, with the remainder returned to the donor.
Autologous donation — Blood donated for the donor's own use.
Granulocytes — White blood cells.
Plasma — The liquid part of blood.
Platelets — Tiny, disklike elements of plasma that promote clotting.</p>



