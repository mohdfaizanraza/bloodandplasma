<p>What is a Plasma Donation?
In a plasma-only donation, the liquid portion of the donor’s blood is separated from the cells. Blood is drawn from one arm and sent through a high-tech machine that collects the plasma. The donor’s red blood cells and platelets are then returned to the donor along with some saline. The process is safe and only takes a few minutes longer than donating whole blood.</p>
<br>

<p>Plasma serves four important functions in our bodies:-

1. Helps maintain blood pressure and volume.

2. Supply critical proteins for blood clotting and immunity.

3. Carries electrolytes such as sodium and potassium to our muscles.

4. Helps to maintain a proper pH balance in the body, which supports cell function.</p>



